export type T = typeof translations.pl

const LangContext = createContext<{ lang: Lang; t: T; setLang: (l: Lang) => void }>({
  lang: 'pl',
  t: translations.pl as unknown as T,
  setLang: () => {},
})

export function LangProvider({ children }: { children: ReactNode }) {
  const stored = (localStorage.getItem('rasci_lang') ?? 'pl') as Lang
  const [lang, setLangState] = useState<Lang>(stored)

  const setLang = (l: Lang) => {
    localStorage.setItem('rasci_lang', l)
    setLangState(l)
  }

  return (
    <LangContext.Provider value={{ lang, t: translations[lang] as unknown as T, setLang }}>
      {children}
    </LangContext.Provider>
  )
}

export function useT() {
  return useContext(LangContext)
}
